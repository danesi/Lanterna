package modelo;

/**
 *
 * @author boemo
 */
public class Contato {
    private int codigoContato;
    private String nomeContato;
    private String numCelular;
    private String numFixo;

    public int getCodigoContato() {
        return codigoContato;
    }

    public void setCodigoContato(int codigoContato) {
        this.codigoContato = codigoContato;
    }

    public String getNomeContato() {
        return nomeContato;
    }

    public void setNomeContato(String nomeContato) {
        this.nomeContato = nomeContato;
    }

    public String getNumCelular() {
        return numCelular;
    }

    public void setNumCelular(String numCelular) {
        this.numCelular = numCelular;
    }

    public String getNumFixo() {
        return numFixo;
    }

    public void setNumFixo(String numFixo) {
        this.numFixo = numFixo;
    }
    
    
    
}
